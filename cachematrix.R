## The fucntions below calculate the inverse of matrix and save them in Cache adn resue theminstead of re caluclating them if the matrix is unchanged

## makeCacheMatrix caluclautes the inverse of matrix and save that in cache memory

makeCacheMatrix <- function(x = matrix()) {
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setInverse <- function(solve) m <<- solve
        getInverse <- function() m
        list(set = set, get = get,
             setInverse = setInverse,
             getInverse = getInverse)
}


## cacheSolve checks if the inverse is in cache memory , if yes it uses it else it calculates it

cacheSolve <- function(x, ...) {
        m <- x$getInverse()
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- solve(data, ...)
        x$setInverse(m)
        m
}
